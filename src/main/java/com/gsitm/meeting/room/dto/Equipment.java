package com.gsitm.meeting.room.dto;

public class Equipment {
	
	private String eqId;
	private String mrId;
	
	public Equipment() {
		super();
	}

	public String getEqId() {
		return eqId;
	}

	public void setEqId(String eqId) {
		this.eqId = eqId;
	}

	public String getMrId() {
		return mrId;
	}

	public void setMrId(String mrId) {
		this.mrId = mrId;
	}
	
	
	

}
